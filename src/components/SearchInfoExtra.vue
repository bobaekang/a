<template>
  <div class="text-xs-center font-lato">
    <div v-if="items.length === 0">Loading {{ contentType }}s...</div>

    <template v-else class="py-2">
      <div>{{ filteredItems.length }} {{ contentType }}s found</div>

      <div v-if="filteredItems.length === 0">
        Need suggestions? Try these:
        <br />
        <span v-for="(suggestion, i) in suggestions" :key="i">
          <v-btn
            flat
            class="ma-1"
            style="text-transform: none;"
            @click="useSuggestion(suggestion)"
          >
            {{ suggestion }}
          </v-btn>
        </span>
      </div>
    </template>
  </div>
</template>

<script>
export default {
  props: {
    contentType: String,
    items: Array,
    filteredItems: Array,
    suggestions: Array
  },
  methods: {
    useSuggestion(suggestion) {
      this.$emit('useSuggestion', suggestion)
    }
  }
}
</script>

